void sumvParallel(float *v1, float *v2, uint32_t dim, float *y){

    uint32_t i;
    uint32_t ncpu1, ncpu2;

    /* Number of adds that each CPU will execute */
    ncpu1 = dim >> 1;
    ncpu2 = dim - ncpu1;

    /* Sends command to secondary CPU */


    for(i = 0; i < dim; i++){
        *y++ = *v1++ + *v2++;
    }
}
//-----------------------------------------------------------------------------
void mulmv(float *m, uint32_t mDim, float *v, uint32_t nDim, float* y){

    uint32_t i, j;

    for(i = 0; i < mDim; i++){
        *y = 0;
        for(j = 0; j < nDim; j++){
            *y += *m++ * v[j];
        }
        y++;
    }
}
